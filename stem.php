        <h2>A. Pengertian</h2>
        <p>Menurut (Bybee, 2013) STEM merupakan sebuah pendekatan yang mengintegrasikan bidang sains, teknologi, rekayasa, dan matematika menjadi satu kesatuan yang utuh dalam prosesnya.</p>
		<p>Menurut (Kinawati, Karniawati, & Suwarna, 2015) Indonesia dan Pendidikan di Indonesia, STEM merujuk pada konsep pengintegrasian desain teknologi dan rekayasa dalam pembelajaran matematika atau sains pada kurikulum sekolah. </p>
		<p>Menurut Revee (dalam Muthiâ€™ik, 2018: 7) menjelaskan bahwa pendidikan STEM sebagai pendekatan interdisiplin, yang di dalamnya siswa dituntut untuk memiliki pengetahuan dan keterampilan pada bidang ilmu pengetahuan, teknologi, rekayasa, dan matematika.</p>
		<p>Berdasarkan pendapat ahli tersebut, dapat disimpulkan jika STEM adalah rancangan pembelajaran dengan mengimplementasikan sains, teknologi, rekayasa, dan matematika untuk mengembangkan keterampilan siswa melalui proses pemecahan masalah dalam kehidupan sehari-hari untuk mendukung kemampuan abad 21.</p>
